package io.github.ankitat.qaforge;

/** A generated test case with optional quality evaluation. */
public final class GeneratedTestCase {
  public final String skillId;
  public final String framework;
  public final String code;
  public final TestMetadata metadata;
  public EvaluationScore evaluation; // nullable

  public GeneratedTestCase(String skillId, String framework, String code, TestMetadata metadata) {
    this.skillId = skillId;
    this.framework = framework;
    this.code = code;
    this.metadata = metadata;
  }
}
